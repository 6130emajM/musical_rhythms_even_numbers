# Musical Rhythm-inspired Even Numbers Program

print("🎵 Welcome to the Rhythm of Even Numbers 🎶")
print("Let's create a smooth rhythm with even numbers between 1 and 20.\n")

# Initialize variables
number = 2  # Start with the smallest even number
beat = 1    # Beat count to simulate rhythm

while number <= 20:
    # Print the number with a rhythmic reference
    print(f"Beat {beat}: 🎶 Even Number - {number}")
    number += 2  # Move to the next even number
    beat += 1    # Increment the beat count

print("\n🎵 The rhythm ends here! Hope you enjoyed the melody of numbers! 🎶")
